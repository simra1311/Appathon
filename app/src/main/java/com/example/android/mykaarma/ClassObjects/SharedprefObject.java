package com.example.android.mykaarma.ClassObjects;

public class SharedprefObject {
    String make, distance, price, lat, lon;

    public SharedprefObject(String make, String distance, String price, String lat, String lon) {
        this.make = make;
        this.distance = distance;
        this.price = price;
        this.lat = lat;
        this.lon = lon;
    }
}
